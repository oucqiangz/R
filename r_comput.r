## R中数据的基本运算

## 基本数学运算 -------------------------------------------
num1 <- 10
num2 <- 6
sum2 <- num1 + num2
print(sum2)
sqrt2 <- num1 ^ 2
print(sqrt2)
mod2 <- num1 %% num2 #求模，得到余数
print(mod2)
zhengchu2 <- num1 %/% num2 #整除，得到商
print(zhengchu2)

# 比较运算 -----------------------------------------------
# >、<、>=、<=、==、!=
if(num1 != num2) print("num1 not equal num2") else print("num1 equal num2")
















